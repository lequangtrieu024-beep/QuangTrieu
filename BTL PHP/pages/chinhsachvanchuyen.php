<h2>CHÍNH SÁCH VẬN CHUYỂN</h2>

<p>Chúng tôi cung cấp dịch vụ giao hàng trên toàn quốc nhằm mang đến trải nghiệm mua sắm thuận tiện nhất cho khách hàng.</p>

<h3>1. Phạm vi giao hàng</h3>
<p>ElectroShop hỗ trợ giao hàng trên toàn lãnh thổ Việt Nam thông qua các đơn vị vận chuyển uy tín.</p>

<h3>2. Thời gian giao hàng</h3>
<ul>
<li>Khu vực nội thành: 1 - 2 ngày làm việc.</li>
<li>Khu vực ngoại thành và các tỉnh: 2 - 5 ngày làm việc.</li>
<li>Thời gian giao hàng có thể thay đổi tùy theo điều kiện thời tiết hoặc đơn vị vận chuyển.</li>
</ul>

<h3>3. Phí vận chuyển</h3>
<ul>
<li>Miễn phí vận chuyển với đơn hàng từ 1.000.000 VNĐ trở lên.</li>
<li>Đơn hàng dưới 1.000.000 VNĐ sẽ áp dụng phí vận chuyển tùy khu vực.</li>
</ul>

<h3>4. Kiểm tra khi nhận hàng</h3>
<p>Khách hàng được quyền kiểm tra sản phẩm trước khi thanh toán. Nếu phát hiện sản phẩm bị hư hỏng hoặc sai mẫu mã, khách hàng có thể từ chối nhận hàng.</p>