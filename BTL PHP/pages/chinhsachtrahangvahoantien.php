<h2>CHÍNH SÁCH TRẢ HÀNG VÀ HOÀN TIỀN</h2>

<p>Chúng tôi cam kết mang đến sản phẩm chất lượng. Nếu sản phẩm gặp vấn đề, khách hàng có thể yêu cầu đổi trả theo các điều kiện dưới đây.</p>

<h3>1. Điều kiện đổi trả</h3>
<ul>
<li>Sản phẩm bị lỗi kỹ thuật từ nhà sản xuất.</li>
<li>Sản phẩm giao sai mẫu, sai màu hoặc sai số lượng.</li>
<li>Sản phẩm còn nguyên vẹn, đầy đủ hộp và phụ kiện.</li>
</ul>

<h3>2. Thời gian đổi trả</h3>
<ul>
<li>Khách hàng có thể yêu cầu đổi trả trong vòng <b>7 ngày</b> kể từ ngày nhận hàng.</li>
</ul>

<h3>3. Trường hợp không hỗ trợ đổi trả</h3>
<ul>
<li>Sản phẩm bị hư hỏng do người sử dụng.</li>
<li>Sản phẩm không còn đầy đủ phụ kiện hoặc bao bì.</li>
<li>Quá thời gian đổi trả quy định.</li>
</ul>

<h3>4. Hoàn tiền</h3>
<p>Tiền hoàn sẽ được xử lý trong vòng <b>3 - 7 ngày làm việc</b> thông qua phương thức thanh toán ban đầu hoặc chuyển khoản ngân hàng.</p>