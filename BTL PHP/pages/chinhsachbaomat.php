<h2>CHÍNH SÁCH BẢO MẬT</h2>

<p>ElectroShop cam kết bảo vệ thông tin cá nhân của khách hàng khi sử dụng website và dịch vụ của chúng tôi.</p>

<h3>1. Thu thập thông tin</h3>
<p>Chúng tôi thu thập các thông tin cần thiết như:</p>
<ul>
<li>Họ và tên</li>
<li>Số điện thoại</li>
<li>Địa chỉ giao hàng</li>
<li>Email</li>
</ul>

<h3>2. Mục đích sử dụng thông tin</h3>
<ul>
<li>Xử lý đơn hàng và giao hàng.</li>
<li>Hỗ trợ khách hàng khi cần thiết.</li>
<li>Cải thiện chất lượng dịch vụ.</li>
</ul>

<h3>3. Bảo mật thông tin</h3>
<p>Chúng tôi cam kết không chia sẻ hoặc bán thông tin cá nhân của khách hàng cho bên thứ ba khi chưa có sự đồng ý.</p>

<h3>4. Quyền của khách hàng</h3>
<p>Khách hàng có quyền yêu cầu chỉnh sửa hoặc xóa thông tin cá nhân bất cứ lúc nào bằng cách liên hệ với bộ phận hỗ trợ.</p>